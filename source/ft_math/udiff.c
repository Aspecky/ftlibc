#include "ft_math.h"

uintmax_t	udiff(uintmax_t a, uintmax_t b)
{
	uintmax_t	tmp;

	if (a < b)
	{
		tmp = a;
		a = b;
		b = tmp;
	}
	return (a - b);
}
